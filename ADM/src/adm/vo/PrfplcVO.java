package adm.vo;
// 공연장 VO클래스
public class PrfplcVO {

	private String prf_code;
	private String prf_nm;
	private String prf_addr;
	private String prf_tel;
	private String prf_city;
	private String prf_gu;
	
	
	public String getPrf_code() {
		return prf_code;
	}
	public void setPrf_code(String prf_code) {
		this.prf_code = prf_code;
	}
	public String getPrf_nm() {
		return prf_nm;
	}
	public void setPrf_nm(String prf_nm) {
		this.prf_nm = prf_nm;
	}
	public String getPrf_addr() {
		return prf_addr;
	}
	public void setPrf_addr(String prf_addr) {
		this.prf_addr = prf_addr;
	}
	public String getPrf_tel() {
		return prf_tel;
	}
	public void setPrf_tel(String prf_tel) {
		this.prf_tel = prf_tel;
	}
	public String getPrf_city() {
		return prf_city;
	}
	public void setPrf_city(String prf_city) {
		this.prf_city = prf_city;
	}
	public String getPrf_gu() {
		return prf_gu;
	}
	public void setPrf_gu(String prf_gu) {
		this.prf_gu = prf_gu;
	}
}
